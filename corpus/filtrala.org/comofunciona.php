											<h2>Cómo funciona</h2>

											<h6>Fíltrala solo acepta materiales restringidos o censurados de relevancia política, científica, ética, filosófica, diplomática o histórica. Cualquier tipo de rumor, opinión, historias o relatos de primera mano, así como material disponible públicamente no serán aceptados. Materiales que violen la privacidad personal no serán aceptados a menos que revelen o prueben violaciones, abusos o corrupción que afecten a la esfera pública. 

Todo el proceso, desde el envío a la publicación, consta de 5 etapas.</h6><!-- Excerpt -->

												<article class="box-excerpt">

													

<h4 class="blue">1. ENVÍO DE MATERIALES</h4>

        <p>

Utilizando nuestra plataforma, cualquier persona puede enviar de manera segura y anónima información sensible que demuestre irregularidades, crímenes, corrupción, abusos e infracciones cometidos por agentes públicos o privados del ámbito español.

La información será aceptada solo si cumple con los requisitos expresados anteriormente.

</p>

<p>        <br><h4 class="blue">2. VERIFICACIÓN</h4>

</p>

<p>

En primer lugar, el equipo de análisis de Fíltrala  realizará una verificación técnica  exhaustiva para determinar si la información enviada es verídica. Esto se hará de la manera más completa posible, analizando factores como posibles causas para una falsificación, la dificultad o motivación para hacerlo etc. Si hubiese la necesidad, se buscarán opiniones externas. </p>

<p>

 <br><h4 class="blue">3. REDACCIÓN Y LIMPIEZA</h4>

</p>

<p>

En algunos casos, los archivos enviados pueden ser una amenaza para la propia identidad de la fuente. Cuando esto sucede, es decir, cuando el contenido o meta-contenido pueden llevar a revelar su identidad, se vuelve necesario eliminar esta información parcialmente. Esto también implica a terceras partes que no están directamente involucradas, ya que de esta manera no se viola su privacidad. El equipo de Fíltrala eliminará, cuando haga falta, toda información que pueda poner en peligro la fuente o terceras partes no involucradas.

</p>

 <br><h4 class="blue">4. ANÁLISIS Y DIFUSIÓN</h4>

</p>

<p>

El análisis de la información en este punto tiene dos pasos. En primer lugar, una investigación en profundidad en lo que suelen ser archivos muy grandes y en formatos complejos, para poder encontrar información relevante. Posteriormente será puesta en contexto, explicando actores, motivos y consecuencias en relación con las organizaciones e instituciones en cuestión. 

</p>

 <br><h4 class="blue">5. PUBLICACIÓN</h4>

</p>

<p>

La publicación de toda pieza de noticias o historia resultante con los miembros de la red de medios colaboradores  de Fíltrala, es decir, con periodistas, docentes e investigadores. Una vez publicado, el material será accesible de manera libre con una licencia CC-by-SA en el canal principal de noticias de Fíltrala. El objetivo es difundir la información de forma amplia para que las revelaciones tengan el mayor impacto posible.

</p>

