											<h2>Sobre</h2>

											<h6>Fíltrala es una herramienta independiente que busca una transformación en la sociedad española a través de la transparencia y la participación ciudadana. Queremos que el público ayude a restaurar el rol activo de los medios como vigilantes del poder, creando una nueva conciencia social donde ciudadanos anónimos puedan influir en lo que es de interés público.</h6><!-- Excerpt -->

												<article class="box-excerpt">

													

<h4 class="blue"> Estamos afincados en Bélgica</h4>

        <p>

Fíltrala forma parte de la red internacional de la Associated Whistleblowing Press, una organización sin ánimo de lucro con sede en Bélgica que se dedica a combatir infracciones, corrupción y violaciones de los derechos humanos a través del periodismo de investigación y de herramientas para la transparencia. Elegimos Bélgica como sede ya que es un país que destaca en garantías legales relacionadas con la libertad de prensa, la protección de fuentes y la protección de la actividad periodística, hasta tal punto que su legislación nos prohíbe revelar nuestras fuentes, en el hipotético caso que las llegaramos a conocer.</p>

<p>        <br><h4 class="blue"> Seguridad en el envío de materiales</h4>

</p>

<p>

Garantizar la seguridad del proceso de envío es esencial. Para ello utilizamos una conjunción de las tecnologías más avanzadas para garantizar que nadie, incluídos nosotros, pueda saber el origen de la información. Fíltrala funciona con tecnología desarrollada por Globaleaks, un sistema de código abierto creado especialmente para enviar y recibir ficheros de manera anónima por Internet. El software funciona junto con la fuertísima encriptación PGP y el Navegador Tor, que permite navegar por Internet de manera anónima y no requiere de conocimientos previos de informática. El proceso de envío está pensado para ser seguro a la vez que simple.</p>

<p>

 <br><h4 class="blue"> Amplificamos tu voz</h4>

</p>

<p>

Fíltrala es parte de una red internacional de activistas, periodistas y  ciudadanos comprometidos en sacar a la luz información de interés público.La documentación recibida a través de Fíltrala será investigada y analizada por nuestra red de colaboradores, formada por organizaciones civiles y medios de comunicación, para su posterior publicación. El objetivo principal es que el impacto de la información revelada sea amplificado al máximo tanto a nivel mediático como civil y legal.

</p>

