a cumplir los intereses de las más grandes corporaciones de la tierra (...) La negociación irrestricta del flujo de datos personales, neutralidad del Internet y firmas eletrónicas puede ser usada para violar el cierne de los derechos individuales. Los gobiernos deben ser claros sobre lo que están negociando en esos tratados secretos'.

