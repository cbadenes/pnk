<a name=19></a>Jan 24, 2015<h4 class="blue"><a href="#19">MORECOWBELL: Programa de la NSA cuestiona la arquitectura básica del Internet</a></h4>

 <p>

Cuando haces algo en Internet, casi todo comienza con una solicitud al DNS. Es gracias a este protocolo fundamental que usuarios sin ningún conocimiento técnico pueden acceder a distintos servicios buscando nombres (como www.ejemplo.com), en lugar de complejos números IP (como 2001:DB8:4145::4242). El DNS fue pensado para resolver un problema fundamental de usabilidad de la incipiente red, y con el tiempo ha sido tan exitoso que es utilizado diariamente por cada persona en todo lo que hace en el ciberespacio.<br><br>Este sistema, construido a inicios de los 80s durante los tiempos optimistas del Internet, nunca tuvo entre sus objetivos proteger la privacidad de sus usuarios: todas las bases de datos son públicas y almacenan el contenido de las transmisiones y otros metadatos sensibles de los usuarios (por ejemplo, la información sobre duración, momento y lugar de acceso) sin ningún tipo de cifrado.<br><br>El DNS siempre ha sido un libro abierto y MORECOWBELL es el programa que la NSA ha desarrollado exclusivamente para leerlo. Como demuestran las diapositivas filtradas, el sistema permite monitorear de forma altamente efectiva la disponibilidad de páginas y servidores web, cambios de contenido y una gran variedad de metadatos que permiten armar perfiles detallados de usuarios y servicios y, en última instancia, encontrar puntos vulnerables para lanzar ataques cibernéticos. Gracias al uso del DNS en todo el Internet público, las implicaciones de este programa de espionaje son enormes, ya que afecta a todos los usuarios a nivel global.<br><br>Para lograr esto, MORECOWBELL utiliza infraestructura dedicada y camuflada en varios países, que incluyen Alemania, Malasia y Dinamarca, además de otros 13 países que sustentan su red de servidores. Este programa distribuido y secreto le permite, por un lado, tener una visión global de las resoluciones DNS y la disponibilidad de los servicios, y, por otro, hacer que sea imposible de atribuir la operación al gobierno de los EE.UU.<br><br>A pesar de que existe conciencia dentro de la comunidad del DNS de que la privacidad es un problema grave, intereses conflictivos hacen virtualmente imposible una solución consensuada. Por una parte, modificar un sistema tan vasto como el DNS puede resultar en problemas mayúsculos para el acceso a Internet de billones de usuarios.  Por otra, una modificación que busque resolver estos problemas puede ser negativo para modelos de negocios importantes o para intereses nacionales.

