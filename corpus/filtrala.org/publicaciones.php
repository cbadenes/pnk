<a name=27></a>Jun 9, 2015<h4 class="blue"><a href="#27">Sanciones y despidos disciplinarios injustos en la ONCE</a></h4>

 <p>

La siguiente publicación es una carta de la Organización Nacional de Ciegos Españoles (ONCE) dirigida a uno de sus trabajadores discapacitados, en la que se le amenaza con 'medidas disciplinarias por falta muy grave' por no llegar al volumen de ventas mínimo. Una cifra muy lejana de lo que gana el trabajador en cuestión. <br><br> Esta situación es particularmente injusta, ya que la ONCE concede la venta de sus productos a estancos, bares o gasolineras, lo cual entra en competencia desleal con los trabajadores y trabajadoras de su plantilla, en detrimento de las personas ciegas. <br><br> Como consecuencia, los vendedores y vendedoras están bajando sus ventas día tras día, llegando muchos de ellos a ser sancionados por la empresa, con sanciones que en casos han llegado al despido, debido a los elevados mínimos de ventas exigidos por la dirección de la empresa y ratificados mediante convenio por los sindicatos UGT y CC.OO. <br><br> Estos mínimos de venta mensual han sido impuestos de manera unilateral sin tener en cuenta la discapacidad del vendedor, la cantidad o afluencia de gente en determinados puntos de venta o el desmantelamiento comercial de zonas de altas ventas.

</p>

